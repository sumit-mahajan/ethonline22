import { executeMutation } from "../../utils/api_service";

export const createPostRequest = async (request) => {
  return await executeMutation(CREATE_POST, request);
};

const CREATE_POST = `mutation($request: CreatePublicPostRequest!) {
    createPostTypedData(request: $request) {
      id
      expiresAt
      typedData {
        types {
          PostWithSig {
            name
            type
          }
        }
        domain {
          name
          chainId
          version
          verifyingContract
        }
        value {
          nonce
          deadline
          profileId
          contentURI
          collectModule
          collectModuleInitData
          referenceModule
          referenceModuleInitData
        }
      }
    }
  }`;
